# Задание-1:
# Напишите функцию, округляющую полученное произвольное десятичное число
# до кол-ва знаков (кол-во знаков передается вторым аргументом).
# Округление должно происходить по математическим правилам (0.6 --> 1, 0.4 --> 0).
# Для решения задачи не используйте встроенные функции и функции из модуля math.


def my_round(number, ndigits):  # по PEP8 должно быть две пустые строки перед def (хотя правило это звучит как то иначе, не помню просто)
    multip = 10**(ndigits + 1)
    tmp_int = int(number * multip)  # а зачем к int приводишь?
    rest = tmp_int % 10
    if (rest<5):  # здесь можно и без скобок, но с пробелами "if rest < 5:"
        return (tmp_int - rest) / multip
    else:
        return (tmp_int + 10 - rest ) / multip  # надо так "(tmp_int + 10 - rest) / multip" следи за пробелами
print('{}'.format(my_round(2.12345676, 7)))  # можно и без format "print(my_round(2.12345676, 7))"


