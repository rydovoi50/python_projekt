# Билет считается счастливым, если сумма его первых и последних цифр равны.
# !!!P.S.: функция не должна НИЧЕГО print'ить


def lucky_ticket(a):  # по PEP8 должно быть две пустые строки перед def (хотя правило это звучит как то иначе, не помню просто)
    a = str(a)
    a1 = a[0:3]
    a2 = a[3:6]
    a1 = int(a1)
    a2 = int(a2)
    summ1 = 0
    while a1 > 0:
        summ1 = summ1 + a1 % 10
        a1 = a1 // 10
    summ2 = 0 
    while a2 > 0:
        summ2 = summ2 + a2 % 10
        a2 = a2 // 10
    return summ1 == summ2 
    

